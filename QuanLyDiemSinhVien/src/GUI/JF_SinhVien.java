    private void tblSinhVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSinhVienMouseClicked
        lstSinhVien = dll_sv.getAllSinhVien();
        int row = tblSinhVien.getSelectedRow();
        txtMSSV.setText(lstSinhVien.get(row).getMaSoSinhVien());
        txtHoTen.setText(lstSinhVien.get(row).getHoTen());
        txtSDT.setText(lstSinhVien.get(row).getSoDienThoai());
        rdNu.setSelected(true);
        if (lstSinhVien.get(row).isGioiTinh()) {
            rdNam.setSelected(true);
        }
        lstLop = dll_lop.getAllLop_dk(lstSinhVien.get(row).getMaLop());
        // method.showMessegaNo(lstKhoa.get(0).getMaKhoa());
        if (lstLop.size() > 0) {
            cbLop.setSelectedItem(lstLop.get(0).getTenLop());
        } else {
            cbLop.setSelectedIndex(0);
        }
        Date date = lstSinhVien.get(row).getNgaySinh();
        txtNgaySinh.setDate(date);
        dk = txtMSSV.getText();
        btnDelete.setEnabled(true);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_tblSinhVienMouseClicked

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        lstLop = dll_lop.getAllLop();
        String mssv = txtMSSV.getText();
        String tensv = txtHoTen.getText();
        boolean gioitinh = true;
        if (rdNu.isSelected()) {
            gioitinh = false;
        }
        int cbRow = cbLop.getSelectedIndex();
        String malop = lstLop.get(cbRow).getMaLop();
        String sdt = txtSDT.getText();
        Date ngaysinh = txtNgaySinh.getDate();

        DBO_SINH_VIEN item = new DBO_SINH_VIEN();
        item.setMaSoSinhVien(mssv);
        item.setHoTen(tensv);
        item.setGioiTinh(gioitinh);
        item.setMaLop(malop);
        item.setSoDienThoai(sdt);
        item.setNgaySinh(ngaysinh);
        if (mssv.isEmpty() || tensv.isEmpty() || malop.isEmpty() || sdt.isEmpty()) {
            method.showMessegaNo("Have a textFiles is Empty");
        } else {
            lstSinhVien = dll_sv.Check_SV(mssv);
            if (lstSinhVien.size() < 1) {
                if (dll_sv.Insert(item)) {
                    method.showMessegaNo("insert data success");
                    res();
                    BidingDataSinhVien();
                } else {
                    method.showMessegaNo("insert data Fail");
                }
            } else {
                method.showMessegaNo("Đã tồn tại dữ liệu trong cơ sở dữ liệu, Xin kiểm tra lại");
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

