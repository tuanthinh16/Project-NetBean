    public void operateFile(String title, int type, int order) { //title = tên hộp thoại, type = save hoặc lưu file, order = 1 thì khi ấn sava ko hỏi lại có muốn save ko
        JFileChooser chooser = new JFileChooser();
        int choose = -1; //biến này là biến khi ấn nút save hay open trong hộp thoại save thì sẽ trả về giá trị JFileChooser.APPROVE_OPTION
        chooser.setDialogTitle(title);
        switch(type) {
            case FILE_OPEN : chooser.showOpenDialog(null); break;
            case FILE_SAVE : chooser.showSaveDialog(null); break;
        }
        if(choose == JFileChooser.APPROVE_OPTION) {
            File file = chooser.getSelectedFile();
            switch(type) {
                case FILE_OPEN : { 
                try {
                    readFile(file);
                } catch (IOException ex) {
                    Logger.getLogger(FileChooserDemo_2.class.getName()).log(Level.SEVERE, null, ex);
                }
                    this.setTitle(file.getName()); break;
                }
                case FILE_SAVE : { 
                    if(order == 2) {
                        int kq = JOptionPane.showConfirmDialog(null, "Bạn có thực sự muốn lưu ko?");
                        if(kq == JOptionPane.YES_OPTION) {
                            writeFile(file);
                            this.setTitle(file.getName());
                        }
                    }
                    else {
                        writeFile(file);
                        this.setTitle(file.getName());
                    }
                    break;
                } 
            }
        }
    }
    
    public void readFile(File file) throws IOException { //hàm này đọc file, ứng dụng trong việc mở 1 file
        tafile.setText(""); //clear tafile, sau đó đọc file vừa chọn để mở
        try {
            FileReader fr = new FileReader(file);
            BufferedReader br = new BufferedReader(fr);
            String line;
            while((line = br.readLine()) != null) {
                tafile.append(line+"\n");
            }
            br.close(); fr.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FileChooserDemo_2.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void writeFile(File file) {
        try {
            file.createNewFile();
            FileWriter fw = new FileWriter(file);
            BufferedWriter bw = new BufferedWriter(fw);
            String data = tafile.getText();
            bw.write(data);
            bw.close(); fw.close();
            changed = false;
        } catch (IOException ex) {
            Logger.getLogger(FileChooserDemo_2.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
