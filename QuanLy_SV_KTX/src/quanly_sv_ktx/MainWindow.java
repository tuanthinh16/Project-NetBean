    private void miQuanLySinhVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_miQuanLySinhVienActionPerformed
        try { 
        //nếu đã đăng nhập rồi thì thực hiện như sau
            if (isDangXuat == false) {
                quyenQuanLyToaNha = lf.getToaNhaDuocPhepQuanLy();
                if (quyenQuanLyToaNha.equals("all")) { //chú ý nếu dùng if (quyenQuanLyToaNha == "all") là sai vì quyenQuanLyToaNha != "all" luôn luôn xảy ra
                    svf = new SinhVienFrame();
                    svf.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
                    svf.setVisible(true); //hiển thị frame sv
                    System.out.println("all");
                } else {
                    svfTheoToaNha = new SinhVienFrameTheoToaNha(quyenQuanLyToaNha);
                    svfTheoToaNha.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
                    svfTheoToaNha.setVisible(true);
                    System.out.println("Hiển thị dssv ở tòa nhà " + svfTheoToaNha.getTenToaNha());
                }
                //nếu dùng hàm: svf.setTenToaNha(quyenQuanLyToaNha); thì lại ko đc. vì bản thân hàm SinhVienFrameTheoToaNha() đã có hàm showDataInTable()
                //do đó khi gọi nó, hàm showDataInTable() đc gọi luôn nên tenToaNha = null ko đổi. khi ta thực hiện lệnh svf.setTenToaNha(quyenQuanLyToaNha) 
                //thì nó thực hiện sau lệnh gọi hàm showDataInTable(), nên hàm showDataInTable() sẽ lấy giá trị tenToaNha trước đó và = null

            } else {
                SinhVienFrameTheoToaNha svf = new SinhVienFrameTheoToaNha(null);
            }
        } catch (Exception e) {
            //ngoại lệ này xảy ra khi chưa đăng nhập mà ấn vào Quản lý sinh viên
            JOptionPane.showMessageDialog(null, "Bạn chưa đăng nhập!", "Lỗi!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_miQuanLySinhVienActionPerformed

